#include "libft.h"

int	ft_nlen(int n)
{
	int i;

	i = 0;
	if (n == 0)
		return (1);
	(n < 0) ? (++i) : 0;
	while (n != 0)
	{
		++i;
		n /= 10;
	}
	return (i);
}
