#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	int		l;
	char	*s;

	i = 1;
	l = ft_nlen(n);
	if (!(s = ft_strnew(l)))
		return (NULL);
	if (n == -2147483648)
		return (ft_strcpy(s, "-2147483648"));
	if (n < 0)
	{
		s[0] = '-';
		n *= -1;
	}
	if (n == 0)
		s[0] = '0';
	while (n > 0)
	{
		s[l - i++] = (n % 10) + 48;
		n /= 10;
	}
	return (s);
}
