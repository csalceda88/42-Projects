#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (c == *s)
			break ;
		++s;
	}
	if (*s == '\0' && c != *s)
		return (0);
	else
		return ((char *)s);
}
