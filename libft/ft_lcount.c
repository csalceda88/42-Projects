#include "libft.h"

int	ft_lcount(char const *s, char c)
{
	int i;
	int r;

	i = 0;
	r = 0;
	while (s[i] == c)
		i++;
	while (s[i] != '\0' && s[i] != c)
	{
		r = r + 1;
		i++;
	}
	return (r);
}
