#include "libft.h"

char	*ft_strcat(char *dest, char const *src)
{
	int	i;
	int x;

	x = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[x] != '\0')
	{
		dest[i] = src[x];
		i++;
		x++;
	}
	dest[i] = '\0';
	return (dest);
}
